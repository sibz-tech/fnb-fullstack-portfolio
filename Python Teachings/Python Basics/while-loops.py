# WHILE Loop Control Statements: Control the flow of execution within loops using 'break', 'continue', and 'pass' statements

count = 0

while count < 5:
    print(count)
    count += 1  # Increment count by 1
    if count == 3:
        break  # Exits out of the loop when loop count reaches 3

