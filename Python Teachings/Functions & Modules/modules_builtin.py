# Use built-in modules in python to break down large programs in to small, manageable and organized files.
# Built-in modules are pre-installed with python and can be imported directly without any installation.
# Modules rely on the reusability of code.

# Math module- provides mathematical functions and constants.
import math
print(math.pi) # Outputs: 3.141592653589793

# Random module- provides functions to generate random numbers.
import random
print(random.randint(0, 10)) # Outputs a random integer between 0 and 10

#DateTime module- provides classes for manipulating dates and times.
import datetime
print(datetime.datetime.now()) # Outputs the current date and time

# OS module- provides functions to interact with the operating system.
import os
print(os.getcwd()) # Outputs the current working directory

# Sys module- provides access to system-specific parameters and functions.
import sys
print(sys.version) # Outputs the Python version being used

