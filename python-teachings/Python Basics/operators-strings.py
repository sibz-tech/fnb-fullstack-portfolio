# Operators with Strings
# Joining two strings together using concatenation (+)
# Repeating a string multiple times using multiplication (*)

str1 = "Hello"
str2 = "World"

# Concatenation
print(str1 + " " + str2) # Using commas ensure spacing
print(str1 + " " + str2 + " " + str2) # Concatenation on multiple strings

# Multiplication (Repeating)
result2 = str1 * 3
print(result2)
