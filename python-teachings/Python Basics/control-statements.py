# Control Statements: If, Else, Elif
# Control statements are used to control the flow of execution in a program.
# They allow you to make decisions and execute certain blocks of code based on conditions.

# If Statement: Code to execute / check if condition is true / positive

num = 0 # When num is positive, the if statement will execute & print the message

if num > 0:
    print("This is a positive number")

elif num == 0:  # Elif Statement needs condions to check / When num is zero, the elif statement will execute & print the message
    print("This number is zero")

# If-Else Statement: Code to execute if condition is true / positive, else code to execute if condition is false / negative
# When num1 is negative, the else statement will execute & print the message
else:
    print("This number is either zero or negative")
    print("This number is negative")




